        /// <returns></returns>
        public static int GetPageCount(int pageSize, int totalCount)
        {
            //计算页数
            if (totalCount < 1 || pageSize < 1)
            {
                return 1;
            }
            int pageCount = totalCount / pageSize;
            if (pageCount < 1)
            {
                return 1;
            }
            if (totalCount % pageSize > 0)
            {
                pageCount += 1;
            }

            return pageCount;
        }

