        public static string GetLngLat(string address, ref string errMsg)
        {
            var jsonText = Geocode(address);
            if (string.IsNullOrEmpty(jsonText))
            {
                errMsg = "无法解析该地址信息";
                return null;
            }
            JObject result = (JObject)JsonConvert.DeserializeObject(jsonText);
            string status = result["status"].ToString();
            string infocode = result["infocode"].ToString();
            if (string.IsNullOrEmpty(status) || string.IsNullOrEmpty(infocode))
            {
                errMsg = "无法解析该地址信息";
                return null;
            }

            if (status.Equals("1") && infocode.Equals("10000"))
            {
                JArray geocodes = (JArray)JsonConvert.DeserializeObject(result["geocodes"].ToString());
                string location = geocodes[0]["location"].ToString();
                return location;
            }
            else
            {
                errMsg = result["info"].ToString();
                return null;
            }
        }

